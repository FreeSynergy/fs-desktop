// ── Reference TOML (compile-time) ─────────────────────────────────────────────

const EN_TOML: &str = include_str!("../assets/i18n/en.toml");

// ── TranslationEntry ──────────────────────────────────────────────────────────

#[derive(Clone, PartialEq, Debug)]
struct TranslationEntry {
    key: String,
    en_value: String,
    /// Current value in the target language (empty = untranslated).
    target_val: String,
    edited: bool,
}

impl TranslationEntry {
    fn is_translated(&self) -> bool {
        !self.target_val.is_empty()
    }
}

// ── Helpers ───────────────────────────────────────────────────────────────────

fn flatten_toml(toml_str: &str) -> Vec<(String, String)> {
    let Ok(table) = toml_str.parse::<toml::Table>() else {
        return Vec::new();
    };
    let mut out = Vec::new();
    flatten_table(&table, "", &mut out);
    out
}

fn flatten_table(table: &toml::Table, prefix: &str, out: &mut Vec<(String, String)>) {
    for (k, v) in table {
        let full_key = if prefix.is_empty() {
            k.clone()
        } else {
            format!("{prefix}.{k}")
        };
        match v {
            toml::Value::String(s) => out.push((full_key, s.clone())),
            toml::Value::Table(t) => flatten_table(t, &full_key, out),
            other => out.push((full_key, other.to_string())),
        }
    }
}

fn entries_to_toml(entries: &[TranslationEntry]) -> String {
    let mut lines = vec![
        "# FreeSynergy translation".to_string(),
        "# Generated by Translation Editor".to_string(),
        String::new(),
    ];
    for e in entries {
        if e.is_translated() {
            let escaped = e.target_val.replace('\\', r"\\").replace('"', r#"\""#);
            lines.push(format!("{} = \"{}\"", e.key, escaped));
        }
    }
    lines.join("\n")
}

fn load_installed_translation(lang_code: &str) -> Vec<(String, String)> {
    let home = std::env::var("HOME").unwrap_or_else(|_| ".".into());
    let path = std::path::PathBuf::from(home)
        .join(".local/share/fsn/i18n")
        .join(lang_code)
        .join("ui.toml");
    let content = std::fs::read_to_string(path).unwrap_or_default();
    flatten_toml(&content)
}

fn build_entries(lang_code: &str) -> Vec<TranslationEntry> {
    let en_pairs = flatten_toml(EN_TOML);
    let tgt_pairs = load_installed_translation(lang_code);
    let tgt_map: std::collections::HashMap<String, String> = tgt_pairs.into_iter().collect();

    en_pairs
        .into_iter()
        .map(|(key, en_value)| {
            let target_val = tgt_map.get(&key).cloned().unwrap_or_default();
            TranslationEntry {
                key,
                en_value,
                target_val,
                edited: false,
            }
        })
        .collect()
}

// ── LLM helpers ───────────────────────────────────────────────────────────────

/// Auto-generates the LLM prompt: EN reference + missing keys.
fn build_llm_prompt(
    lang_name: &str,
    lang_code: &str,
    entries: &[TranslationEntry],
    user_request: &str,
) -> String {
    let missing: Vec<&TranslationEntry> = entries
        .iter()
        .filter(|e| !e.is_translated())
        .take(50)   // Limit to avoid token overflow
        .collect();

    let mut prompt = format!(
        "You are a professional software translator for the FreeSynergy open-source project.\n\
         Translate the following UI text strings into {lang_name} ({lang_code} locale).\n\
         Return ONLY a flat TOML block — one line per key, format: \"key.name\" = \"translation\"\n\
         No explanations, no code fences, no comments.\n\n\
         Strings to translate:\n"
    );

    for e in &missing {
        let escaped = e.en_value.replace('"', r#"\""#);
        let _ = writeln!(prompt, "\"{}\" = \"{}\"", e.key, escaped);
    }

    if !user_request.trim().is_empty() {
        let _ = write!(prompt, "\nAdditional instruction: {}", user_request.trim());
    }

    prompt
}

/// Parses the LLM response into `(key, value)` proposal pairs.
fn parse_llm_proposals(response: &str) -> Vec<(String, String)> {
    let mut proposals = Vec::new();

    // Strip markdown code fences if present
    let body = response
        .trim()
        .trim_start_matches("```toml")
        .trim_start_matches("```")
        .trim_end_matches("```")
        .trim();

    for line in body.lines() {
        let line = line.trim();
        if line.is_empty() || line.starts_with('#') {
            continue;
        }
        // Match:  "key.subkey" = "value"  or  key.subkey = "value"
        if let Some(eq_pos) = line.find(" = \"") {
            let raw_key = line[..eq_pos].trim().trim_matches('"').to_string();
            let value_start = eq_pos + 4;
            if value_start < line.len() {
                let tail = &line[value_start..];
                let value = tail.trim_end_matches('"').to_string();
                if !raw_key.is_empty() {
                    proposals.push((raw_key, value));
                }
            }
        }
    }

    proposals
}

/// Calls the configured LLM service and returns the raw text response.
async fn call_llm_service(service_name: &str, prompt: String) -> Result<String, String> {
    let client = reqwest::Client::builder()
        .timeout(std::time::Duration::from_secs(120))
        .build()
        .map_err(|e| e.to_string())?;

    if service_name == "ollama" {
        let resp = client
            .post("http://localhost:11434/api/generate")
            .json(&serde_json::json!({
                "model": "llama3.2",
                "prompt": prompt,
                "stream": false
            }))
            .send()
            .await
            .map_err(|e| e.to_string())?;

        let json: serde_json::Value = resp.json().await.map_err(|e| e.to_string())?;
        json["response"]
            .as_str()
            .map(std::string::ToString::to_string)
            .ok_or_else(|| "Unexpected Ollama response format".to_string())
    } else {
        // Generic OpenAI-compatible API (llama.cpp server, LM Studio, etc.)
        let resp = client
            .post("http://localhost:8080/v1/chat/completions")
            .json(&serde_json::json!({
                "messages": [{ "role": "user", "content": prompt }],
                "stream": false
            }))
            .send()
            .await
            .map_err(|e| e.to_string())?;

        let json: serde_json::Value = resp.json().await.map_err(|e| e.to_string())?;
        json["choices"][0]["message"]["content"]
            .as_str()
            .map(std::string::ToString::to_string)
            .ok_or_else(|| "Unexpected API response format".to_string())
    }
}

