/// Translation Editor — side-by-side EN reference vs. target language.
///
/// Shows all translation keys from the English reference TOML, lets the user
/// edit the target language values inline, and provides:
///   - Progress bar (translated / total keys)
///   - Filter: missing only, keyword search
///   - "Export .toml" for everyone
///   - "Commit & Push" for authenticated GitHub contributors
///   - "AI Translate" when an LLM role is configured in Service Roles
use std::fmt::Write as _;

